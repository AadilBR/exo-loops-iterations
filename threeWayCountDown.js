console.log('Ceci est une boucle do...while :')

let i = 101

do {
  console.log(i)
  i = i - 2

} while (i > 0)



console.log('\nCeci est une boucle while :')

let j = 101

while (j > 0) {
  console.log(j)
  j -= 2
}



console.log('\nCeci est une boucle for :')

for (k = 101; k > 0; k -= 2) {
  console.log(k)
}